
# ide al usuario dos números y muestra la suma, resta, multiplicación y división (separadas por coma).

numero_uno = float(input("Ingrese un número: "))
numero_dos = float(input("Ingrese un segundo número: "))

suma = numero_uno + numero_dos
resta = numero_uno - numero_dos
x =  numero_uno * numero_dos
div = numero_uno / numero_dos

print (f"Los resultados son: S:{suma}, R:{resta}, M:{x}, D:{div}.")