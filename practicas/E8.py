
# Pide al usuario dos números y muestra la división entera (//) y el módulo (%) entre ellos.

numero_uno = float(input("Ingresa un número: "))
numero_dos = float(input("Ingresa un número: "))
division = numero_dos // numero_dos
mod = numero_uno % numero_dos
print(f"La división de sus números es: {division} y el módulo es: {mod}")
