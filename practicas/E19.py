
# Pide al usuario tres números, calcula el promedio y muestra el resultado.

numero_uno = float(input("Ingresa un número: "))
numero_dos = float(input("Ingresa un número: "))
numero_tres = float(input("Ingresa un número: "))

promedio = ((numero_uno + numero_dos + numero_tres)/3)
print (f"{promedio:.1f}")